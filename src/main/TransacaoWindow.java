    private void EnviarMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_EnviarMouseClicked
        // TODO add your handling code here:
        PreparedStatement ps = null;
        String sql = "";
        boolean is_editing = Editando.isSelected();

        try {
            String tipo = Tipo.getSelectedItem().toString();
            String valor = Valor.getText().replace(".", "").replace(",", ".");
            System.out.println(valor);
            TransacaoItem itemFromImovelBox = (TransacaoItem) ImovelBox.getSelectedItem();
            TransacaoItem itemFromClienteBox = (TransacaoItem) ClienteBox.getSelectedItem();
            String imovel_box = Integer.toString(itemFromImovelBox.getId());
            String cliente_box = Integer.toString(itemFromClienteBox.getId());

            if (!is_editing) {
                sql = "INSERT INTO Transacao (venda, aluguel, id_imovel, id_cliente) VALUES (?, ?, ?, ?)";
            } else {
                sql = "UPDATE Transacao SET venda = ?, aluguel = ?, id_imovel = ?, id_cliente = ? WHERE id_transacao = ?";
            }

            ps = Conexao.getConexao().prepareStatement(sql);
            
            if (!"Venda".equals(tipo)) {
                ps.setString(1, null);
                ps.setString(2, valor);
            } else {
                ps.setString(1, valor);
                ps.setString(2, null);
            }
            
            ps.setString(3, imovel_box);
            ps.setString(4, cliente_box);

            if (is_editing) {
                ps.setString(5, jTable2.getValueAt(jTable2.getSelectedRow(), 0).toString());
            }

            boolean b = ps.execute();
            if (!b) {
                if (is_editing) {
                    JOptionPane.showMessageDialog(this, "Transação editada com sucesso!");
                } else {
                    JOptionPane.showMessageDialog(this, "Transação criada com sucesso!");
                }
                formWindowOpened(null);
            } else {
                JOptionPane.showMessageDialog(this, "Erro! Tente novamente mais tarde...");
            }
            Transacao.setVisible(false);
            ps.close();
        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_EnviarMouseClicked

