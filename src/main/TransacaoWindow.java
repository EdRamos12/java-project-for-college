            String sql = "SELECT Transacao.id_transacao, Transacao.venda, Transacao.aluguel, Imovel.endereco_imovel, Cliente.nome_cliente FROM Transacao JOIN Cliente ON Transacao.id_cliente = Cliente.id_cliente JOIN Imovel ON Transacao.id_imovel = Imovel.id_imovel";
            
