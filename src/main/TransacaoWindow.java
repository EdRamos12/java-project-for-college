    private void jTable2MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTable2MouseClicked
        int id = Integer.parseInt(jTable2.getValueAt(jTable2.getSelectedRow(), 0).toString());

        try {
            String sql = "SELECT * FROM Transacao WHERE id_transacao="+id;
            Connection conn = Conexao.getConexao();

            Statement st = conn.createStatement(ResultSet.TYPE_SCROLL_INSENSITIVE,
                ResultSet.CONCUR_UPDATABLE);
            ResultSet res = st.executeQuery(sql);

            Editar.setEnabled(true);
            Excluir.setEnabled(true);

            while (res.next()) {
                if (res.getObject("venda") == null) {
                    Valor.setText(res.getString("aluguel"));
                    Tipo.setSelectedIndex(1);
                } else {
                    Valor.setText(res.getString("venda"));
                    Tipo.setSelectedIndex(0);
                }
                
            }

        } catch (Exception e) {
            e.printStackTrace();
        }
    }//GEN-LAST:event_jTable2MouseClicked

